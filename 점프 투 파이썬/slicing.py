# 슬라이싱으로 문자열 바꾸기  * 슬라이스로 만든 뒤 바꿀 문자열을 더해 줌
a = "pithon"
a[:1]   # 0번째 문자열부터 0번째 문자열을 슬라이싱
a[2:]   # 2번째 문자열부터  마지막 문자열까지 슬라이싱
print(a[:1]+ 'y' + a[2:])

