# 처음부터 끝까지 출력
a = "Life is too short, You need Python"
print(a[:])     # 처음부터 끝까지 모두 슬라이싱   * -0은 첫 번째 숫자를 의미, -1 마지막 직전의 숫자를 의미.

# 원하는 자료만 출력
